import pandas as pd
def translate_champion(chinese_name):
    """
    Translate a single Chinese champion name to English.
    If the name is not found in the map, return the original name.
    """
    return CHAMPION_TRANSLATION_MAP.get(chinese_name, chinese_name)

def translate_list(chinese_list):
    """
    Translate a list of Chinese champion names to English.
    """
    return [translate_champion(name) for name in chinese_list]

def translate_dataframe(df, column_name):
    """
    Translate a specific column in a pandas DataFrame from Chinese to English.
    """
    if column_name not in df.columns:
        raise ValueError(f"Column '{column_name}' does not exist in the DataFrame")
    df[column_name] = df[column_name].apply(translate_champion)
    return df

def translate_csv(input_path, output_path, column_name):
    """
    Read a CSV file, translate a specified column from Chinese to English,
    and save the result to a new CSV file.
    """
    df = pd.read_csv(input_path)
    df = translate_dataframe(df, column_name)
    df.to_csv(output_path, index=False)
    print(f"Translated CSV saved to '{output_path}'")

